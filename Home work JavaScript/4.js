/* Дано число n = 1000. Поділіть його на 2 стільки разів,
 поки результат поділу не буде меньше 50. Яке число вийде? 
 Порахуйте кількість ітерацій, необхідних для цього.
 */

var bu = 1000
var b = 0

for (bu; ; bu/=2){
    b++
    if (bu<50) break
}
console.log('Число: ' + bu)
console.log('Кількість ітерацій: ' + b)